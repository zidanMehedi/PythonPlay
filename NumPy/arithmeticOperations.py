import numpy as np


#1D array operations
arr1 = np.array([2,4,6,8,10])
print(arr1+2)
print(arr1-2)
print(arr1*2)
print(arr1/2)
print(arr1**2)
print(arr1%2)
print()
print(np.add(arr1,2))
print(np.subtract(arr1,2))
print(np.multiply(arr1,2))
print(np.divide(arr1,2))
print(np.power(arr1,2))
print(np.mod(arr1,2))
print()


arr2 = np.array([20,40,60,80,100])
